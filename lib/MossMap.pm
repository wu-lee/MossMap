package MossMap;
use strict;
use warnings;

=head1 NAME

MossMap - a map of (bryological) observations

=head1 SYNOPSIS

This is a Mojolicious::Lite app with an AngularJS front end.

Run the app like this:

    moss-map.pl

By default it will listen on port 3000.  Alternatively use
Mojolicious' morbo or hypnotoad, see the Mojolicious documentation for
more details.

=head1 DESCRIPTION

=head2 CONFIGURATION

There is an example configuration file moss-map.example.json, you can
copy it into place as moss-map.json and modify as required.

